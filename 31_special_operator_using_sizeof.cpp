#include<bits/stdc++.h>
using namespace std;
int main()
{
    int a;
    float f;
    double d;
    char ch;
    char name[20];
    cout<<sizeof(a)<<endl;
    cout<<sizeof(f)<<endl;
    cout<<sizeof(d)<<endl;
    cout<<sizeof(ch)<<endl;
    cout<<sizeof(name)<<endl;
}
/*
    int     4 bit
    float   4 bit
    double  8 bit
    char    1 bit
    name    20 bit

*/